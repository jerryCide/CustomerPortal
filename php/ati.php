      <p>The Access To Information             Act</p>
      <p align="justify">The             Access to Information Act 2002 is the legislation enacted by the government             of Jamaica to grant the right of access to records in the custody of             Government Ministries, Agencies, Departments and Public bodies, to groups,             organizations and the ordinary Jamaican citizens. This act aims to promote             transparency, government accountability and public participation in             national decision-making.</p>
