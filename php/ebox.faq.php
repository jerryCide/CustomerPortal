  <strong>FREQUENTLY ASKED  QUESTIONS</strong></p>
<ol>
  <li><strong>What training will be provided for managers and supervisors? </strong></li>
</ol>
<p><strong><em><u>Ans:</u></em></strong><br />
  Persons who have the responsibility for managing/supervising and appraising other members of <br />
  staff will be trained in two key areas: </p>
<ol>
  <li>In the steps involved in the performance management and appraisal process - <br />
    setting and agreeing performance targets (work plan development), monitoring and managing performance during the year and conducting the formal appraisal at the end of the year. </li>
  <li>In understanding their role as the appraiser and the soft skills required, to ensure that performance is managed amicably from the stage of agreeing targets, to formal appraisal at the end of the year. </li>
  <li><strong>How does this system differ from what obtains now? </strong></li>
</ol>
<p><strong><em><u>An</u></em></strong><strong><em><u>s:</u></em></strong></p>
<ol>
  <li>There is now an objective basis for the appraisal - targets agreed based on discussions between the jobholder and their manager/supervisor. </li>
  <li>Targets agreed will relate to the actual work the job holder is involved in, and how the work to be done contributes to the achievement of departmental/unit objectives and subsequently the ministry's/ entity's corporate objectives. </li>
</ol>
<ol>
  <li>You will now be appraised on the output you achieve <strong><u>and</u></strong><u> </u>the manner in which you achieve that output (competencies demonstrated), rather than on purely subjective factors. </li>
  <li>The results of the appraisal will provide useful information with respect to performance improvement, continuous professional development and career management. </li>
  <li><strong>What recourse do I have if I have an appeal against the Performance Management and Appraisal System - not the outcome of my personal appraisal, but a principle on which the system is based? </strong></li>
</ol>
<p><strong><em><u>A</u></em></strong><strong><em><u>n</u></em></strong><strong><em><u>s</u></em></strong><strong><em><u>:</u></em></strong><br />
  The Performance Management Implementation Team (PMIT) that leads the implementation process Service-wide, will review all appeals against the system. Any such appeals should be brought to the attention of the Senior Human Resource Director with responsibility for your Ministry/Department, who will then route it through the appropriate channel. </p>
<ol>
  <li><strong>What recourse do I have if I disagree with the end of year appraisal of my performance? </strong></li>
</ol>
<p><strong><em><u>An</u></em></strong><strong><em><u>s</u></em></strong><strong><em><u>:</u></em></strong><br />
  The PMAS Guideline and Reference Manual and this Handbook outline the process that should be followed. Ask your HRM Division for the details. </p>
<ol>
  <li><strong>When will I have my first appraisal using the new system? </strong></li>
</ol>
<p><strong><em><u>A</u></em></strong><strong><em><u>n</u></em></strong><strong><em><u>s:</u></em></strong><br />
  It is recommended that the first appraisal should be an informal (but documented) review by your supervisor/manager <strong>three</strong> months after the initial work plan is agreed, and quarterly thereafter (meeting notes signed by both parties is sufficient). Some entities however have chosen to hold this first documented review <strong>six</strong> months after agreeing the initial work plan. While signed meeting notes are sufficient, some organisations opt to record interim evaluation on the appraisal form. <br />
  <strong>f. Will the payment of my increment still be automatic, as has been the practice? </strong><br />
  <strong><em><u>An</u></em></strong><strong><em><u>s</u></em></strong><strong><em><u>:</u></em></strong><br />
  No. It will be based on the results of the end of year performance appraisal. In keeping with the Staff Orders, it will be based on satisfactory performance. The PMAS states that an overall score of at least 75% must be achieved for an increment to be paid. </p>
<ol>
  <li><strong>If my performance has been affected by circumstances beyond my control, will it be taken into consideration? </strong></li>
</ol>
<p><strong><em><u>An</u></em></strong><strong><em><u>s</u></em></strong><strong><em><u>:</u></em></strong><br />
  Yes, it will be taken into consideration. </p>

<div>
  <p>At the time of the interim evaluation(s) (quarterly/half-yearly and as otherwise necessary), the supervisor/manager will provide feedback to the employee on their performance and facilitate improvement, where necessary, with the help of the Human Resource Management Department. </p>
  <p>If at the time of the formal end of year appraisal, an employee's performance is still considered poor even though efforts have been made as agreed to facilitate performance improvement and <br />
    <strong>b</strong>. the outcome of the performance appraisal is signed off by the reviewing manager and </p>
  <p><strong>c</strong><strong>.</strong> there is no appeal pending, </p>
  <p>the employee's salary increment will not be paid if a score of at least 75% is not achieved. It <br />
    should also be noted that Section 14.4.3 of the Staff Orders states the following: </p>
  <p>&quot;Following confirmation of permanent employment, an employee may have his/her service <br />
    terminated if his/her performance on the job is assessed over two years to be consistently below <br />
    established performance standards or expectation.&quot; </p>
  <p>Until such a decision has been taken, the HR Department will work along with the employee and <br />
    the supervisor in order to facilitate improvements in performance. </p>
  <p><strong>i. What can I expect if I meet the targets  agreed 100%? </strong></p>
  <p><strong><em><u>An</u></em></strong><strong><em><u>s</u></em></strong><strong><em><u>:</u></em></strong></p>
  <p>The release of your next increment due would be approved and you would be eligible for special recognition and a reward in keeping with the established Recognition and Reward Policy Framework. Ask your HRM Division for more details on this. </p>
  <p>&nbsp;</p>
  <p>&nbsp;</p>
  <p><strong>j. What can I expect if I meet most  of the targets agreed? </strong></p>
  <p><strong><em><u>An</u></em></strong><strong><em><u>s</u></em></strong><strong><em><u>:</u></em></strong></p>
  <p>If you have achieved an overall score of 75% - 79%, the release of your next increment due would be approved. If you have achieved a score 2: 80% you will also be eligible for a particular form of recognition or/and reward in keeping with the established Recognition and Reward Policy Framework. Ask your HRM Division for more details on this. </p>
  <p>&nbsp;</p>
  <ol>
    <li><strong>What can I expect if  I have achieved an overall score of 75% but I am already at the top of my salary scale? </strong></li>
  </ol>
  <p><strong><em><u>An</u></em></strong><strong><em><u>s</u></em></strong><strong><em><u>:</u></em></strong></p>
  <p>If your organisation has implemented the PMAS, you would be eligible for a one-off payment. Ministry of Finance and Planning issued a circular with respect to this. Ask your HRM Division for more details. </p>
