<p>The  Ministry of Water and Housing is responsible for, </p>
<ol start="1" type="1">
  <li>The provision of potable water, proper       sewage facilities and related services to all users and;</li>
  <li>The facilitation of affordable housing       solutions for Jamaicans.</li>
</ol>
<p>In  achieving its mandate, the Ministry of Water and Housing has its strategic  objectives effectively aligned with the direction of the Government. As part of its approach for providing housing  solutions for its citizenry, the Government intends to ensure that housing  solutions will cater to the needs and affordability of different markets. Some of the measures by which the government  intends to achieve this include:</p>
<ul type="square">
  <li>providing more structured options that       will cater to different needs; two such options include build-on-own-land       solutions to assist persons to build on land that they already own; and       open-market purchases, among others;</li>
  <li>providing land for housing development,       with an aim to reduce and eliminate the emergence of informal settlements; also referred to as squatter communities. Also, the government will seek to provide alternative sites where       squatter communities can be relocated;</li>
  <li>identifying more affordable construction       materials and methods; and </li>
  <li>reviewing the Rent Restriction Act with       a view to increasing the hosing stock that is available for rental; among       others.</li>
</ul>
<p>&nbsp;</p>
<p>Subsequently,  the housing portfolio of the Ministry has as its objectives, to:</p>
<ul type="square">
  <li>providing affordable housing for low       income earners;</li>
  <li>upgrading and regularize the tenure of       residents of squatter settlements;</li>
  <li>developing sustainable communities by addressing       the social and physical infrastructure and also by protecting the       environment;</li>
  <li>develop a National Housing Policy; and</li>
  <li>amend the Rent Restriction Act.</li>
</ul>
<p>&nbsp;</p>
<p>As  it relates to the provision of water, the Government is resolute in its effort  to improve the management of this precious commodity, and increase the supply  and distribution of same, within the framework of the National Water Sector  Policy (1999). Water is life and is  required to facilitate development, and to improve living conditions for all  Jamaicans. As such the Government  intends to:</p>
<ul type="square">
  <li>conduct assessments of available water       resources, in order to identify water projects that will satisfy current       and future demands;</li>
  <li>implement new water supply projects       based on needs;</li>
  <li>implement water conservation and       management programmes; among others.</li>
</ul>
<p>Subsequently,  the objectives of the Ministry as it relates to the provision of water include:</p>
<ul type="square">
  <li>the sustainable management of water       resources;</li>
  <li>installing new water systems and       improving existing ones;</li>
  <li>improved irrigation facilities for       farming communities; and</li>
  <li>improved efficiency and effectiveness in       the utilization of this resource.<strong></strong></li>
</ul>
<p>It  is the intention of the Ministry to as best as possible utilize its resources  during this planning period (2008/2011), to ensure the achievement of the  policies as will be embarked on by the Government of Jamaica. It is the  Ministry's intention to so strategically align itself with these priorities and  international development goals in order to continue to be efficient and  effective at all levels of service delivery to the Jamaican populace.</p>
<p>This document provides a summary of the programmes,  projects and activities to be executed by each Unit/Directorate/Department and  Portfolio entities of the Ministry of Water and Housing during the course of  the next financial year, 2008/09. <strong>The operational plan, together  with the financial plan (budget) and the corporate plan are the basic guides  for the Ministry and its portfolio entities and their respective operations.  The Operational Plan will therefore form the basis for reporting on  performance. </strong>Detailed information on the Corporate and  Operational Plans may be had from the Strategic Planning and Evaluation Unit of  the Ministry.</p>
